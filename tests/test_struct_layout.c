/* ---- FID-0085: grenade-round projectile-init-matrix hand field (manual) ----
 * sub_GAME_7F05F73C (grenade launcher / flare pistol / piton gun round spawn)
 * copies the projectile init matrix from &hands[hand].throw_item_pos_related.
 * Retail US ASM reads it as the raw N64 byte offset g_CurrentPlayer + hand*936
 * + 0xAD8, i.e. hands@0x870 + field@+0x268. On the 64-bit port layout, pointer
 * fields inside struct hand (audioHandle, rocket, ...) before that field expand
 * 4->8B, so the field is NOT at N64 0x268 and the hand stride is NOT 936. The
 * native gun.c body (post-fix) MUST reach it by C field access, never the raw
 * offset. These asserts lock the actual PC layout and, critically, prove the
 * raw N64 offset the legacy (GE007_NO_PROJECTILE_INIT_MTX_FIX) path uses does
 * NOT coincide with the real field — so the default-ON fix is load-bearing.
 * Regenerate expected values with tools/fidelity/gen_struct_asserts.py if the
 * layout intentionally changes; a drift here is a fix-invalidating finding. */
#define FID0085_N64_HAND_STRIDE   936     /* ASM *936 strength-reduce            */
#define FID0085_N64_HANDS_BASE    0x870   /* addiu $t9,$t8,0x870                 */
#define FID0085_N64_MTX_FIELD     0x268   /* 0xAD8 - 0x870 (raw path within-hand)*/
