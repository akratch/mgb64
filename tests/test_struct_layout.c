/* The FID-0085/0087/0088/0091/0092/0094 manual locks below all test the SAME
 * underlying phenomenon: retail N64 code baked in raw byte offsets assuming
 * 4-byte pointers; on this port's LP64 build, pointer members inside
 * struct hand / struct player widen to 8 bytes, so those raw offsets no
 * longer coincide with the real (named) fields — that divergence is exactly
 * what makes the default-ON fixes load-bearing. Under ILP32 (wasm32) that
 * divergence does not exist: pointers are 4 bytes, matching retail exactly,
 * so struct hand/player collapse back to (at least very close to) the retail
 * layout the raw offsets were written against — confirmed empirically:
 * sizeof(struct hand) == 936 (the retail N64 stride) under wasm32, so the
 * FID-0094 "!=" stride check legitimately does NOT hold there. These locks
 * are therefore LP64-only: they assert that a 64-bit-pointer-widening
 * divergence exists, which is a meaningless (and false) question to ask on a
 * 32-bit pointer target. Gating the whole block out under ILP32 is not a
 * blanket-gate of convenience — every assert in it is definitionally about
 * the LP64 vs. retail-N64 pointer-width gap. */
